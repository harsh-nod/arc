/// A taint label tracking the provenance/trust of data.
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub enum TaintLabel {
    /// Trusted program-internal data.
    Trusted,
    /// Data originating from user input.
    UserInput,
    /// Data from a third-party or external source.
    ThirdParty,
    /// Data generated by an LLM or model.
    ModelGenerated,
    /// Data from an untrusted network source.
    NetworkInput,
    /// Custom application-defined label.
    Custom(String),
}

impl TaintLabel {
    /// Returns true if this label represents untrusted data.
    pub fn is_untrusted(&self) -> bool {
        !matches!(self, Self::Trusted)
    }

    pub fn as_str(&self) -> &str {
        match self {
            Self::Trusted => "trusted",
            Self::UserInput => "user_input",
            Self::ThirdParty => "third_party",
            Self::ModelGenerated => "model_generated",
            Self::NetworkInput => "network_input",
            Self::Custom(s) => s.as_str(),
        }
    }

    pub fn parse(s: &str) -> Self {
        match s {
            "trusted" => Self::Trusted,
            "user_input" => Self::UserInput,
            "third_party" => Self::ThirdParty,
            "model_generated" => Self::ModelGenerated,
            "network_input" => Self::NetworkInput,
            other => Self::Custom(other.to_string()),
        }
    }
}

impl std::fmt::Display for TaintLabel {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(self.as_str())
    }
}

