    #[test]
    fn taint_propagation_through_ops() {
        let mut module = Module::new(Symbol::new("test"));
        let mut func = Function::new(
            Symbol::new("main"),
            vec![],
            vec![
                Argument {
                    name: ValueId::new("a"),
                    ty: Type::new("i64"),
                    location: loc(),
                },
                Argument {
                    name: ValueId::new("b"),
                    ty: Type::new("i64"),
                    location: loc(),
                },
            ],
            Some(Type::new("i64")),
            loc(),
        );
        let mut block = Block::new(Some("entry".into()), loc());
        block.add_op(Operation {
            results: vec![ValueId::new("c")],
            kind: OperationKind::Add,
            operands: vec![ValueId::new("a"), ValueId::new("b")],
            result_types: vec![Type::new("i64")],
            effects: vec![],
            location: loc(),
            regions: vec![],
        });
        block.add_op(Operation {
            results: vec![],
            kind: OperationKind::Return,
            operands: vec![ValueId::new("c")],
            result_types: vec![],
            effects: vec![],
            location: loc(),
            regions: vec![],
        });
        func.add_block(block);
        module.add_function(func).unwrap();

        let mut sources = HashMap::new();
        sources.insert("a".to_string(), TaintLabel::UserInput);

        let result = propagate_taints(&module, &sources);

        // "a" is tainted, "b" is not, but "c = a + b" should inherit taint from "a".
        assert!(result.get("a").unwrap().contains(&TaintLabel::UserInput));
        assert!(!result.contains_key("b"));
        assert!(result.get("c").unwrap().contains(&TaintLabel::UserInput));
    }

