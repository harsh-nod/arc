/// Compute the set of tainted values reachable from initial taint sources.
/// Returns a map from value name to the set of taint labels it carries.
pub fn propagate_taints(
    module: &Module,
    sources: &HashMap<String, TaintLabel>,
) -> HashMap<String, HashSet<TaintLabel>> {
    let mut taints: HashMap<String, HashSet<TaintLabel>> = HashMap::new();

    for (val, label) in sources {
        taints.entry(val.clone()).or_default().insert(label.clone());
    }

    for (_name, func) in &module.functions {
        // Seed params.
        for param in &func.params {
            let v = param.name.as_str().to_string();
            if let Some(label) = sources.get(&v) {
                taints.entry(v).or_default().insert(label.clone());
            }
        }

        for block in &func.blocks {
            for op in &block.ops {
                let mut merged: HashSet<TaintLabel> = HashSet::new();
                for operand in &op.operands {
                    if let Some(t) = taints.get(operand.as_str()) {
                        merged.extend(t.iter().cloned());
                    }
                }
                if !merged.is_empty() {
                    for result in &op.results {
                        taints
                            .entry(result.as_str().to_string())
                            .or_default()
                            .extend(merged.iter().cloned());
                    }
                }
            }
        }
    }

    taints
}

