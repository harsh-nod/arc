// Security labels
// ---------------------------------------------------------------------------

/// Classification level for data flowing through an AIR program.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub enum SecurityLevel {
    /// Freely observable, no restrictions.
    Public,
    /// Internal use only, may not flow to external sinks.
    Internal,
    /// Sensitive data, requires explicit declassification.
    Confidential,
    /// Highest classification, never flows outside the trust boundary.
    Secret,
}

impl SecurityLevel {
    /// Returns true if `self` can flow into a sink at level `target` without
    /// declassification.
    pub fn can_flow_to(self, target: SecurityLevel) -> bool {
        // Higher-classified data cannot flow to lower-classified sinks.
        self <= target
    }

    pub fn as_str(self) -> &'static str {
        match self {
            Self::Public => "public",
            Self::Internal => "internal",
            Self::Confidential => "confidential",
            Self::Secret => "secret",
        }
    }

    pub fn parse(s: &str) -> Option<Self> {
        match s {
            "public" => Some(Self::Public),
            "internal" => Some(Self::Internal),
            "confidential" => Some(Self::Confidential),
            "secret" => Some(Self::Secret),
            _ => None,
        }
    }
}

impl std::fmt::Display for SecurityLevel {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(self.as_str())
    }
}

