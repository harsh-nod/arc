/// Violations detected by the information-flow analysis.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum SecurityViolation {
    /// Data at `source_level` was sent to a sink requiring `sink_level`.
    IllegalFlow {
        value: String,
        source_level: SecurityLevel,
        sink_level: SecurityLevel,
    },
    /// Tainted data was used in a privileged position (e.g., as an instruction).
    TaintedUse {
        value: String,
        taint: TaintLabel,
        context: String,
    },
    /// Capability invoked outside sandbox policy.
    SandboxViolation { capability: String, reason: String },
    /// Credential value leaked to a logging or tracing sink.
    CredentialLeak { value: String },
    /// Authority token used for a different resource than it was granted for.
    AuthorityMismatch {
        token: String,
        expected: String,
        actual: String,
    },
}

impl std::fmt::Display for SecurityViolation {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::IllegalFlow {
                value,
                source_level,
                sink_level,
            } => write!(
                f,
                "illegal flow: {} ({}) -> {} sink",
                value, source_level, sink_level
            ),
            Self::TaintedUse {
                value,
                taint,
                context,
            } => write!(
                f,
                "tainted value {} ({}) used in privileged context: {}",
                value, taint, context
            ),
            Self::SandboxViolation { capability, reason } => {
                write!(f, "sandbox violation: {} — {}", capability, reason)
            }
            Self::CredentialLeak { value } => {
                write!(f, "credential value {} leaked to sink", value)
            }
            Self::AuthorityMismatch {
                token,
                expected,
                actual,
            } => write!(
                f,
                "authority mismatch: {} granted for {}, used for {}",
                token, expected, actual
            ),
        }
    }
}

/// Run information-flow analysis on a module.
///
/// This performs a forward dataflow pass over each function, propagating
/// security levels and taint labels through operations, and checking that
/// no data flows to a lower-classified sink without declassification.
pub fn check_information_flow(
    module: &Module,
    ctx: &SecurityContext,
    sandbox: Option<&SandboxPolicy>,
) -> Vec<SecurityViolation> {
    let mut violations = Vec::new();

    for (_name, func) in &module.functions {
        // Build per-value security level map for this function, starting from
        // the context's annotations and propagating through operations.
        let mut levels: HashMap<String, SecurityLevel> = HashMap::new();
        let mut taints: HashMap<String, HashSet<TaintLabel>> = HashMap::new();

        // Seed from function parameters.
        for param in &func.params {
            let v = param.name.as_str().to_string();
            levels.insert(v.clone(), ctx.level(&v));
            let t = ctx.taints(&v);
            if !t.is_empty() {
                taints.insert(v, t);
            }
        }

        for block in &func.blocks {
            for arg in &block.args {
                let v = arg.name.as_str().to_string();
                levels.insert(v.clone(), ctx.level(&v));
                let t = ctx.taints(&v);
                if !t.is_empty() {
                    taints.insert(v, t);
                }
            }

            for op in &block.ops {
                // Propagate: result inherits the max level of all operands.
                let operand_level = op
                    .operands
                    .iter()
                    .map(|o| {
                        levels
                            .get(o.as_str())
                            .copied()
                            .unwrap_or(SecurityLevel::Public)
                    })
                    .max()
                    .unwrap_or(SecurityLevel::Public);

                // Merge taints from all operands.
                let mut merged_taints: HashSet<TaintLabel> = HashSet::new();
                for o in &op.operands {
                    if let Some(t) = taints.get(o.as_str()) {
                        merged_taints.extend(t.iter().cloned());
                    }
                }

                for result in &op.results {
                    let result_name = result.as_str();
                    let result_level = std::cmp::max(operand_level, ctx.level(result_name));
                    levels.insert(result_name.to_string(), result_level);

                    let mut result_taints = merged_taints.clone();
                    result_taints.extend(ctx.taints(result_name));
                    if !result_taints.is_empty() {
                        taints.insert(result_name.to_string(), result_taints);
                    }
                }

                // Check capability invocations against sandbox.
                if let OperationKind::Invoke { capability } = &op.kind {
                    let cap_name = capability.as_str();

                    let effective_effects = operation_effects(module, op);

                    if let Some(policy) = sandbox {
                        if !policy.is_allowed(cap_name) {
                            violations.push(SecurityViolation::SandboxViolation {
                                capability: cap_name.to_string(),
                                reason: "capability not permitted by sandbox policy".to_string(),
                            });
                        }
                        for effect in &effective_effects {
                            if policy.is_denied(effect) {
                                violations.push(SecurityViolation::SandboxViolation {
                                    capability: cap_name.to_string(),
                                    reason: format!(
                                        "effect {} is denied by sandbox policy",
                                        effect
                                    ),
                                });
                            }
                        }
                    }

                    // External effects on confidential+ data is an illegal flow
                    // to a public sink (the external world).
                    if has_external_effects(&effective_effects) {
                        for operand in &op.operands {
                            let lev = levels
                                .get(operand.as_str())
                                .copied()
                                .unwrap_or(SecurityLevel::Public);
                            if lev > SecurityLevel::Public && !ctx.is_declassified(operand.as_str())
                            {
                                violations.push(SecurityViolation::IllegalFlow {
                                    value: operand.as_str().to_string(),
                                    source_level: lev,
                                    sink_level: SecurityLevel::Public,
                                });
                            }
                        }
                    }

                    // Credential effect check: credential data should not flow to
                    // logging/tracing sinks.
                    if effective_effects.iter().any(|e| e.as_str() == "credential") {
                        for operand in &op.operands {
                            let lev = levels
                                .get(operand.as_str())
                                .copied()
                                .unwrap_or(SecurityLevel::Public);
                            if lev >= SecurityLevel::Confidential {
                                violations.push(SecurityViolation::CredentialLeak {
                                    value: operand.as_str().to_string(),
                                });
                            }
                        }
                    }
                }

                // Taint check: model-generated or third-party content used as
                // an authority/instruction operand in RequireApproval.
                if matches!(op.kind, OperationKind::RequireApproval) {
                    for operand in &op.operands {
                        if let Some(t) = taints.get(operand.as_str()) {
                            for label in t {
                                if label.is_untrusted() {
                                    violations.push(SecurityViolation::TaintedUse {
                                        value: operand.as_str().to_string(),
                                        taint: label.clone(),
                                        context: "require_approval instruction".to_string(),
                                    });
                                }
                            }
                        }
                    }
                }
            }
        }
    }

    violations
}

/// Check if a set of effect strings contains any external effect.
fn has_external_effects(effects: &[String]) -> bool {
    for e in effects {
        // Parse "#arc.effect<...>" or bare effect names.
        let name = if let Some(inner) = e.strip_prefix("#arc.effect<") {
            inner.strip_suffix('>').unwrap_or(inner)
        } else {
            e.as_str()
        };
        if let Some(eff) = Effect::parse(name) {
            if eff.is_external() {
                return true;
            }
        }
    }
    false
}

fn operation_effects(module: &Module, op: &arc_ir::Operation) -> Vec<String> {
    let mut effects = op.effects.clone();
    if let OperationKind::Invoke { capability } = &op.kind {
        if let Some(cap) = module.capabilities.get(capability) {
            for effect in &cap.effects {
                if !effects.contains(effect) {
                    effects.push(effect.clone());
                }
            }
        }
    }
    effects
}

