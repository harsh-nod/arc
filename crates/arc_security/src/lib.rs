/// Summary of a module's security posture.
#[derive(Debug)]
pub struct SecurityAudit {
    /// Total number of capability invocations.
    pub capability_invocations: usize,
    /// Total number of approval requests.
    pub approval_requests: usize,
    /// Capabilities invoked.
    pub capabilities_used: HashSet<String>,
    /// Whether any operation has external effects.
    pub has_external_effects: bool,
    /// Whether any operation handles credentials.
    pub handles_credentials: bool,
    /// Violations found.
    pub violations: Vec<SecurityViolation>,
}

/// Audit a module for security properties.
pub fn audit_module(
    module: &Module,
    ctx: &SecurityContext,
    sandbox: Option<&SandboxPolicy>,
) -> SecurityAudit {
    let mut audit = SecurityAudit {
        capability_invocations: 0,
        approval_requests: 0,
        capabilities_used: HashSet::new(),
        has_external_effects: false,
        handles_credentials: false,
        violations: Vec::new(),
    };

    for (_name, func) in &module.functions {
        for block in &func.blocks {
            for op in &block.ops {
                match &op.kind {
                    OperationKind::Invoke { capability } => {
                        audit.capability_invocations += 1;
                        audit
                            .capabilities_used
                            .insert(capability.as_str().to_string());
                    }
                    OperationKind::RequireApproval => {
                        audit.approval_requests += 1;
                    }
                    _ => {}
                }

                let effects = operation_effects(module, op);
                if has_external_effects(&effects) {
                    audit.has_external_effects = true;
                }
                if effects.iter().any(|e| e.contains("credential")) {
                    audit.handles_credentials = true;
                }
            }
        }
    }

    audit.violations = check_information_flow(module, ctx, sandbox);
    audit
}

