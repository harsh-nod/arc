/// An attenuated capability: a capability that has been narrowed in scope.
#[derive(Debug, Clone)]
pub struct AttenuatedCapability {
    /// The original capability name.
    pub original: String,
    /// Restricted operations (subset of the original's operations).
    pub allowed_operations: HashSet<String>,
    /// Maximum data level that may pass through.
    pub max_data_level: SecurityLevel,
}

impl AttenuatedCapability {
    pub fn new(original: impl Into<String>, max_level: SecurityLevel) -> Self {
        Self {
            original: original.into(),
            allowed_operations: HashSet::new(),
            max_data_level: max_level,
        }
    }

    pub fn allow_operation(&mut self, op: impl Into<String>) {
        self.allowed_operations.insert(op.into());
    }

    /// Validate that an invocation is within the attenuated scope.
    pub fn validate(&self, operation: &str, data_level: SecurityLevel) -> Result<(), String> {
        if !self.allowed_operations.is_empty() && !self.allowed_operations.contains(operation) {
            return Err(format!(
                "operation '{}' not permitted by attenuated capability '{}'",
                operation, self.original
            ));
        }
        if !data_level.can_flow_to(self.max_data_level) {
            return Err(format!(
                "data level {} exceeds attenuated capability max level {}",
                data_level, self.max_data_level
            ));
        }
        Ok(())
    }
}

