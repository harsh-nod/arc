/// A sandbox policy restricting which capabilities a scope can access.
#[derive(Debug, Clone)]
pub struct SandboxPolicy {
    /// Capabilities explicitly allowed (exact names or glob patterns).
    allowed: HashSet<String>,
    /// Capabilities explicitly denied.
    denied: HashSet<String>,
    /// Maximum security level that may be accessed within the sandbox.
    max_level: SecurityLevel,
}

impl SandboxPolicy {
    pub fn new(max_level: SecurityLevel) -> Self {
        Self {
            allowed: HashSet::new(),
            denied: HashSet::new(),
            max_level,
        }
    }

    pub fn allow(&mut self, capability: impl Into<String>) {
        self.allowed.insert(capability.into());
    }

    pub fn deny(&mut self, capability: impl Into<String>) {
        self.denied.insert(capability.into());
    }

    /// Check if a capability is permitted under this sandbox.
    pub fn is_allowed(&self, capability: &str) -> bool {
        // Explicit deny takes priority.
        if self.is_denied(capability) {
            return false;
        }
        // If an allow-list exists, capability must be in it.
        if !self.allowed.is_empty() {
            return self.allowed.contains(capability);
        }
        true
    }

    /// Check if a capability or effect name is explicitly denied.
    pub fn is_denied(&self, name: &str) -> bool {
        if self.denied.contains(name) {
            return true;
        }
        for pattern in &self.denied {
            if let Some(prefix) = pattern.strip_suffix(".*") {
                if name.starts_with(prefix) {
                    return true;
                }
            }
        }
        false
    }

    /// Check if a security level is permitted within this sandbox.
    pub fn level_allowed(&self, level: SecurityLevel) -> bool {
        level <= self.max_level
    }
}

