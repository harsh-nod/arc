/// Associates SSA values with security levels and taint labels.
#[derive(Debug, Clone, Default)]
pub struct SecurityContext {
    /// Classification level per value.
    levels: HashMap<String, SecurityLevel>,
    /// Taint labels per value (a value can carry multiple taints).
    taints: HashMap<String, HashSet<TaintLabel>>,
    /// Declassification proofs that have been verified.
    declassifications: HashSet<String>,
}

impl SecurityContext {
    pub fn new() -> Self {
        Self::default()
    }

    /// Set the security level for a value.
    pub fn set_level(&mut self, value: &str, level: SecurityLevel) {
        self.levels.insert(value.to_string(), level);
    }

    /// Get the security level for a value, defaulting to Public if unknown.
    pub fn level(&self, value: &str) -> SecurityLevel {
        self.levels
            .get(value)
            .copied()
            .unwrap_or(SecurityLevel::Public)
    }

    /// Add a taint label to a value.
    pub fn add_taint(&mut self, value: &str, label: TaintLabel) {
        self.taints
            .entry(value.to_string())
            .or_default()
            .insert(label);
    }

    /// Get all taint labels for a value.
    pub fn taints(&self, value: &str) -> HashSet<TaintLabel> {
        self.taints.get(value).cloned().unwrap_or_default()
    }

    /// Returns true if the value has any untrusted taint.
    pub fn is_tainted(&self, value: &str) -> bool {
        self.taints
            .get(value)
            .map(|t| t.iter().any(|l| l.is_untrusted()))
            .unwrap_or(false)
    }

    /// Record a declassification proof for a value.
    pub fn add_declassification(&mut self, value: &str) {
        self.declassifications.insert(value.to_string());
    }

    /// Check if a value has been declassified.
    pub fn is_declassified(&self, value: &str) -> bool {
        self.declassifications.contains(value)
    }
}

